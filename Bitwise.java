

public  class Bitwise
{
    public static void main(String[] args) {
        int a = 2 , b=3;
        System.out.println("Bitwise and : "+(a&b));
        System.out.println("Bitwise or : " +(a|b));
        System.out.println("Bitwise Xor : "+(a^b));
        System.out.println("bitwise not : "+(~a));
    }
}