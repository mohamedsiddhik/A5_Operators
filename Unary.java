

public  class Unary
{
    public static void main(String[] args) {
        int a=10,b=20;
        System.out.println("++++++++++++++++++++++++++");
        System.out.println(a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println("---------------------------");
        System.out.println(b);
        System.out.println(b--);
        System.out.println(b);
        System.out.println(--b);
    }
}